package controflow;

public class SwitchCase {

	public static void main(String[] args) {
    //Rules
		byte b=40;
		final byte c=20;
		switch(b) {
		case 10:System.out.println("Yashvani");
		case c:System.out.println("Ankush");
		case 30:System.out.println("Kavya");
		case 40:System.out.println("Anushka");
		default :System.out.println("Addy");
		
		}
	}

}
