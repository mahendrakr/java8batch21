package operator;

public class BitwiseOperators {

	public static void main(String[] args) {
    System.out.println(4 & 5);
    System.out.println(5 & 6);
    System.out.println(4 | 5);
    System.out.println(5 | 6);
    System.out.println(4 ^ 5);
    System.out.println(5 ^ 6);
    System.out.println(3 & 4);
    
    System.out.println(false && true);
    
    
    
	}

}
